package com.example.sample;


public class CircleMove {
	int r, x, y, speed_x, speed_y, color;

	public CircleMove(int r, int x, int y, int speed_x, int speed_y, int color) {
		super();
		this.r = r;
		this.x = x;
		this.y = y;
		this.speed_x = speed_x;
		this.speed_y = speed_y;
		this.color = color;
	}
}
